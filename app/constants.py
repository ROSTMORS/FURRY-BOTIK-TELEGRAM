def build_rp_pattern():
    keywords = sorted(RP_ALIAS.keys(), key=len, reverse=True)
    escaped = [re.escape(k) for k in keywords]
    return re.compile(r"^(" + "|".join(escaped) + r")\s*(@\S+)?\s*$", re.IGNORECASE)

